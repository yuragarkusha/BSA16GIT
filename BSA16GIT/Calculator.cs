using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BSA16GIT
{
    public static class Calculator
    {
        public static int Plus(int a, int b)
        {
            int c = a + b;
            return c;
        }

        public static int Minus(int a, int b)
        {
            int c = a - b;
            return c;
        }

        public static int Multiply(int a, int b)
        {
            int c = a * b;
            return c;
        }
    }
}
